    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        String url = "home.jsp";
        try {
            String action = request.getParameter("action");
            if ("selectSeat".equals(action)) {
                url = handleSelectSeat(request, response);
            } else if ("confirmBooking".equals(action)) {
                url = handleConfirmBooking(request, response);
            } else if ("bookingHistory".equals(action)) {
                url = handleBookingHistory(request, response);
            } else if ("cancelBooking".equals(action)) {
                url = handleCancelBooking(request, response);
            } else {
                url = "login.jsp";
                request.setAttribute("message", "Invalid action " + action);
            }
        } catch (Exception e) {
            e.printStackTrace();
            request.setAttribute("message", "System error occurred!");
        } finally {
            if (url != null) {
                request.getRequestDispatcher(url).forward(request, response);
            }
        }
    }

